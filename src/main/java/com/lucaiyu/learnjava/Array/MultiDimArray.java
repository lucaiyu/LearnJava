package main.java.com.lucaiyu.learnjava.Array;

public class MultiDimArray {
    public static void main(String[] args) {
        int [] [] dim2 = new int[2] [2];
        dim2[0][0] = 0;
        dim2[0][1] = 1;
        dim2[1][0] = 1;
        dim2[1][1] = 0;
        System.out.println(dim2[1][1]);
    }
}
