package main.java.com.lucaiyu.learnjava.If;

public class Switch {
    public static void main(String[] args) {
        int a = 1;
        switch (a){
            case 0 :
                System.out.println("0");
                break;

            case 1 :
                System.out.println("1");
                break;

            case 2 :
                System.out.println("2");
                break;

            default :
                System.out.println("ERROR");
        }
    }
}
