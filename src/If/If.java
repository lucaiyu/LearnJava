package If;

public class If {
    public static void main(String[] args) {
        int a = 1;
        if(a == 0){
            System.out.println("A is 0");
        } else if (a == 1) {
            System.out.println("A is 1");
        }else if (a == 2) {
            System.out.println("A is 2");
        }else{
            System.out.println("A is 3");
        }
    }
}
